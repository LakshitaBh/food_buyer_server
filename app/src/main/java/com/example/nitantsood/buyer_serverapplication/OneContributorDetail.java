public class OneContributorDetail{
    String contributor_UID;
    String food;
    String quantity;
    String name;

    public String getContributor_UID() {
        return contributor_UID;
    }

    public void setContributor_UID(String contributor_UID) {
        this.contributor_UID = contributor_UID;
    }

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
